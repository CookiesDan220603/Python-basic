print(np.linalg.norm(a, 1))

b = np.array([[-2, 8],[3, 1]])
print(np.linalg.norm(b, 1))

c = np.array([[2, -8], [3, 1]])
print(np.linalg.norm(c, 1))

d = np.array([[2, 3], [1, -1]])
print(np.linalg.norm(d, 1))

